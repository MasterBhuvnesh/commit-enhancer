/**
 * @file A simple utility script to create or update a test file.
 * This is used by the E2E test runner to generate a file change that can be committed.
 */

import fs from "fs";
import path from "path";

const data = {
  lastTestRun: new Date().toISOString(),
  message: "This file is automatically generated by the test runner.",
};

// Construct the file path in the project's root directory.
const filePath = path.join(process.cwd(), "testing.json");

// Write the data to the file, creating it if it doesn't exist.
// JSON.stringify with a '2' argument formats the JSON with an indentation of 2 spaces.
fs.writeFileSync(filePath, JSON.stringify(data, null, 2));

console.log("âœ… Created/updated testing.json for the test run.");
